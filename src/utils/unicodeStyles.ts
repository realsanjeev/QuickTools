export function applyStyle(text: string, styleKey: string): string {
  const styleMap = unicodeStyles[styleKey]
  if (!styleMap) return text

  return text.split('').map(char => styleMap[char] || char).join('')
}

export function countStyledChars(text: string, styleKey: string): number {
  const styleMap = unicodeStyles[styleKey]
  if (!styleMap) return 0

  return text.split('').filter(char => styleMap[char]).length
}